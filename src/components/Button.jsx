import React from 'react'

const Button = ({className, size='default', children}) => {
  const baseClasses = "relative overflow-hidden rounded-full font-medium transition-colors duration-300 focus:outline-none  focus-visible:ring-primary bg-primary text-primary-foreground hover:bg-primary/60 shadow-primary/90"
  const sizeClasses = {
    sm: "px-4 py-2 text-sm",
    default: "px-6 py-3 text-base",
    lg: "px-8 py-4 text-lg",
  }
  const classes = `${baseClasses} ${sizeClasses[size] || sizeClasses.default} ${className || ''}`;
  return (
    <button className={classes}>
        <span className='relative flex items-center justify-center gap-2'>{children}</span>
    </button>
  )
}

export default Button